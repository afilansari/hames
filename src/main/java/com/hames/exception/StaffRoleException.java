package com.hames.exception;

public class StaffRoleException extends RuntimeException{

	public StaffRoleException() {
		super();
	}

	public StaffRoleException(String message, Throwable cause) {
		super(message, cause);
	}

	public StaffRoleException(String message) {
		super(message);
	}

	public StaffRoleException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1583543309511634307L;
	
}
